static void SymbolSynchronizer_symbolSyncCodegen(const emlrtStack *sp,
  comm_SymbolSynchronizer *obj, creal_T x[11226], emxArray_creal_T *y, boolean_T
  *overflowFlag, real_T *sampleIdx)
{
  static real_T dv[12] = { 0.0, -0.5, 0.5, 0.0, 1.5, -0.5, 1.0, -0.5, -0.5, 0.0,
    -0.5, 0.5 };

  emlrtStack st;
  creal_T dcv[12];
  creal_T xSeq[4];
  creal_T b_x[3];
  creal_T b_y[3];
  creal_T intOut;
  real_T e;
  real_T t1_re;
  real_T t2_im;
  real_T t2_re;
  int32_T b_i;
  int32_T b_sampleIdx;
  int32_T c_sampleIdx;
  int32_T i;
  boolean_T b_overflowFlag;
  boolean_T c_x;
  boolean_T exitg1;
  boolean_T x_idx_1;
  obj->pNumStrobe = 0.0;
  b_overflowFlag = false;
  b_sampleIdx = 1;
  c_sampleIdx = 0;
  exitg1 = false;
  while ((!exitg1) && (c_sampleIdx < 11226)) {
    b_sampleIdx = c_sampleIdx + 1;
    if ((obj->pNumStrobe == 6175.0) && obj->pStrobe) {
      b_overflowFlag = true;
      exitg1 = true;
    } else {
      obj->pNumStrobe += (real_T)obj->pStrobe;
      st.site = &y_emlrtRSI;
      xSeq[0].re = x[c_sampleIdx].re;
      xSeq[0].im = x[c_sampleIdx].im;
      for (b_i = 0; b_i < 3; b_i++) {
        xSeq[b_i + 1].re = obj->pInterpFilterState[b_i].re;
        xSeq[b_i + 1].im = obj->pInterpFilterState[b_i].im;
      }

      for (i = 0; i < 12; i++) {
        dcv[i].re = dv[i];
        dcv[i].im = 0.0;
      }

      for (i = 0; i < 3; i++) {
        b_y[i].re = 0.0;
        b_y[i].im = 0.0;
        for (b_i = 0; b_i < 4; b_i++) {
          b_y[i].re += dcv[i + 3 * b_i].re * xSeq[b_i].re - dcv[i + 3 * b_i].im *
            xSeq[b_i].im;
          b_y[i].im += dcv[i + 3 * b_i].re * xSeq[b_i].im + dcv[i + 3 * b_i].im *
            xSeq[b_i].re;
        }
      }

      t1_re = obj->pMu;
      b_x[0].re = b_y[0].re;
      b_x[0].im = b_y[0].im;
      b_x[1].re = obj->pMu * b_y[1].re;
      b_x[1].im = obj->pMu * b_y[1].im;
      b_x[2].re = t1_re * t1_re * b_y[2].re;
      b_x[2].im = t1_re * t1_re * b_y[2].im;
      intOut = sumColumnB(b_x);
      for (b_i = 0; b_i < 3; b_i++) {
        obj->pInterpFilterState[b_i].re = xSeq[b_i].re;
        obj->pInterpFilterState[b_i].im = xSeq[b_i].im;
      }

      if (obj->pStrobe) {
        i = (int32_T)emlrtIntegerCheckR2012b(obj->pNumStrobe, &emlrtDCI, (void *)
          sp);
        obj->pSymbolHolder[emlrtDynamicBoundsCheckR2012b(i, 1, 6175, &emlrtBCI,
          (void *)sp) - 1].re = intOut.re;
        i = (int32_T)emlrtIntegerCheckR2012b(obj->pNumStrobe, &emlrtDCI, (void *)
          sp);
        obj->pSymbolHolder[emlrtDynamicBoundsCheckR2012b(i, 1, 6175, &emlrtBCI,
          (void *)sp) - 1].im = intOut.im;
      }

      if (obj->pNumStrobe > 6175.0) {
        b_overflowFlag = true;
        exitg1 = true;
      } else {
        st.site = &ab_emlrtRSI;
        if (obj->pStrobe) {
          c_x = !obj->pStrobeHistory[1];
          if (c_x) {
            t1_re = obj->pTEDBuffer[1].re;
            e = obj->pTEDBuffer[1].im;
            t2_re = obj->pTEDBuffer[1].re;
            t2_im = obj->pTEDBuffer[1].im;
            t2_re += t1_re;
            t1_re = e + t2_im;
            if (t1_re == 0.0) {
              t2_re /= 2.0;
              t1_re = 0.0;
            } else if (t2_re == 0.0) {
              t2_re = 0.0;
              t1_re /= 2.0;
            } else {
              t2_re /= 2.0;
              t1_re /= 2.0;
            }

            e = t2_re * (obj->pTEDBuffer[0].re - intOut.re) + t1_re *
              (obj->pTEDBuffer[0].im - intOut.im);
          } else {
            e = 0.0;
          }
        } else {
          e = 0.0;
        }

        c_x = obj->pStrobeHistory[1];
        x_idx_1 = obj->pStrobe;
        switch ((int32_T)c_x + (int32_T)x_idx_1) {
         case 0:
          break;

         case 1:
          t1_re = obj->pTEDBuffer[1].re;
          t2_re = obj->pTEDBuffer[1].im;
          obj->pTEDBuffer[0].re = t1_re;
          obj->pTEDBuffer[0].im = t2_re;
          obj->pTEDBuffer[1].re = intOut.re;
          obj->pTEDBuffer[1].im = intOut.im;
          break;

         default:
          obj->pTEDBuffer[0].re = 0.0;
          obj->pTEDBuffer[0].im = 0.0;
          obj->pTEDBuffer[1].re = intOut.re;
          obj->pTEDBuffer[1].im = intOut.im;
          break;
        }

        st.site = &bb_emlrtRSI;
        t1_re = obj->pLoopPreviousInput + obj->pLoopFilterState;
        t2_re = e * obj->pProportionalGain + t1_re;
        obj->pLoopFilterState = t1_re;
        obj->pLoopPreviousInput = e * obj->pIntegratorGain;
        st.site = &cb_emlrtRSI;
        c_x = obj->pStrobeHistory[1];
        obj->pStrobeHistory[0] = c_x;
        obj->pStrobeHistory[1] = obj->pStrobe;
        obj->pStrobe = (obj->pNCOCounter < t2_re + 0.5);
        if (obj->pStrobe) {
          obj->pMu = obj->pNCOCounter / (t2_re + 0.5);
        }

        t1_re = obj->pNCOCounter - (t2_re + 0.5);
        if (muDoubleScalarIsNaN(t1_re) || muDoubleScalarIsInf(t1_re)) {
          t2_re = rtNaN;
        } else if (t1_re == 0.0) {
          t2_re = 0.0;
        } else {
          t2_re = muDoubleScalarRem(t1_re, 1.0);
          if (t2_re == 0.0) {
            t2_re = 0.0;
          } else if (t1_re < 0.0) {
            t2_re++;
          }
        }

        obj->pNCOCounter = t2_re;
        c_sampleIdx++;
      }
    }
  }

  t1_re = obj->pNumStrobe;
  if (1.0 > t1_re) {
    c_sampleIdx = -1;
  } else {
    i = (int32_T)emlrtIntegerCheckR2012b(t1_re, &b_emlrtDCI, (void *)sp);
    c_sampleIdx = emlrtDynamicBoundsCheckR2012b(i, 1, 6175, &b_emlrtBCI, (void *)
      sp) - 1;
  }

  i = y->size[0];
  y->size[0] = c_sampleIdx + 1;
  emxEnsureCapacity_creal_T1(y, i);
  for (i = 0; i <= c_sampleIdx; i++) {
    y->data[i].re = obj->pSymbolHolder[i].re;
    y->data[i].im = obj->pSymbolHolder[i].im;
  }

  *overflowFlag = b_overflowFlag;
  *sampleIdx = (real_T)b_sampleIdx;
}

static creal_T sumColumnB(creal_T x[3])
{
  creal_T y;
  int32_T k;
  y.re = x[0].re;
  y.im = x[0].im;
  for (k = 0; k < 2; k++) {
    y.re += x[k + 1].re;
    y.im += x[k + 1].im;
  }

  return y;
}

